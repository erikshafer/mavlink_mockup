-- COURSE
CREATE TABLE COURSE (
  COURSE_ID INT(8) AUTO_INCREMENT PRIMARY KEY,
  COURSE_NAME VARCHAR(255),
  COURSE_CODE VARCHAR(16),
  COURSE_DESCRIPTION TEXT,
  COURSE_CREDITS INT(8),
  COURSE_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
);

-- USER
CREATE TABLE USER (
  USER_ID INT(8) AUTO_INCREMENT PRIMARY KEY,
  USERNAME VARCHAR(255),
  USER_PASSWORD VARCHAR(255),
  USER_FIRST_NAME VARCHAR(50),
  USER_LAST_NAME VARCHAR(50),
  USER_MAJOR VARCHAR(255),
  USER_EMAIL VARCHAR(255),
  USER_ENABLED BOOLEAN NOT NULL DEFAULT TRUE
);

-- ROLE
CREATE TABLE ROLE (
  ROLE_ID INT(11) NOT NULL AUTO_INCREMENT PRIMARY KEY,
  NAME varchar(255) DEFAULT 'ROLE_USER',
);

-- USER ROLE (many-to-many between 'user' and 'role')
CREATE TABLE USER_ROLE (
  USER_ROLE_ID bigint(20) AUTO_INCREMENT PRIMARY KEY,
  ROLE_ID int(11) DEFAULT NULL,
  USER_ID bigint(20) DEFAULT NULL,
);